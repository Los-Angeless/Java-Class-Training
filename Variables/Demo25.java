class Demo25 {
  public static void main(String[] args) {
   char var1 = 'a';
   System.out.println(var1);         //  char
   System.out.println((int)var1);    // -> unicode -> int
   
   char var2 = 'b';
   System.out.println(var2);         //  char
   System.out.println((int)var2);    // -> unicode -> int
   
   char var3 = 'A';
   System.out.println(var3);         //  char
   System.out.println((int)var3);    // -> unicode -> int
   
   char var4 = 'B';
   System.out.println(var4);         //  char
   System.out.println((int)var4);    // -> unicode -> int
    
   
  }
}