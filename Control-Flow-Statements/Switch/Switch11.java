class Switch11
{
 public static void main(String[] args)
 {
  char s = 97;
  switch(s)
  
       {
        case 'a' : System.out.println("Inside a");
	    break;
	    
	    case 'b' : System.out.println("Inside b");
	    break;
	    
	    default : System.out.println("Inside default");
	    break;    
       }
 }
}