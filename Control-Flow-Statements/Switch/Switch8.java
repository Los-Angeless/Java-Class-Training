// we can use default and cases anywhere within switch sttmnt

class Switch8
{
 public static void main(String[] args)
 { 
  int x = 4;
  switch(x)
	 {
		case 1: System.out.println("Inside case one");
		break;
		
		default: System.out.println("Inside deault");
		break;
		
		case 2:  System.out.println("Inside case two");
		break;
		
		case 3: System.out.println("Inside case three");
		break;
		
	 }
 
 }
}